    private void jButtonADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonADDActionPerformed
        // TODO add your handling code here:
        if (!validasiInput()) {
            return;
        }

        String kodeMK = jTextFieldKodeMK.getText().trim();
        String nama = jTextFieldNama.getText().trim();
        String sks = jTextFieldSKS.getText().trim();
        String semester = jComboBoxSM.getSelectedItem().toString();

        String sql = "INSERT INTO matakuliah (kode_mk, nama_mk, sks, semester) VALUES (?, ?, ?, ?)";

        try (Connection conn = DBconnection.getConnection();
                PreparedStatement ps = conn.prepareStatement(sql)) {

            ps.setString(1, kodeMK);
            ps.setString(2, nama);
            ps.setString(3, sks);
            ps.setString(4, semester);
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan.");
            tampilkanData();
            resetForm();

        } catch (SQLException ex) {
            logger.log(java.util.logging.Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Gagal menambah data: " + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
                                              
    }//GEN-LAST:event_jButtonADDActionPerformed

