    private void jButtonUPDATEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUPDATEActionPerformed
        // TODO add your handling code here:
        if (selectedKodeMK == null) {
            JOptionPane.showMessageDialog(this, "Pilih dulu data pada tabel yang ingin diupdate.",
                    "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (!validasiInput()) {
            return;
        }

        String nama = jTextFieldNama.getText().trim();
        String sks = jTextFieldSKS.getText().trim();
        String semester = jComboBoxSM.getSelectedItem().toString();

        String sql = "UPDATE matakuliah SET nama_mk = ?, sks = ?, semester = ? WHERE kode_mk = ?";

        try (Connection conn = DBconnection.getConnection();
                PreparedStatement ps = conn.prepareStatement(sql)) {

            ps.setString(1, nama);
            ps.setString(2, sks);
            ps.setString(3, semester);
            ps.setString(4, selectedKodeMK);
            int affected = ps.executeUpdate();

            if (affected > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil diupdate.");
            } else {
                JOptionPane.showMessageDialog(this, "Data tidak ditemukan.", "Info", JOptionPane.WARNING_MESSAGE);
            }
            tampilkanData();
            resetForm();

        } catch (SQLException ex) {
            logger.log(java.util.logging.Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Gagal update data: " + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
                                                 
    }//GEN-LAST:event_jButtonUPDATEActionPerformed

    private void jButtonDELETEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDELETEActionPerformed
        // TODO add your handling code here:
        if (selectedKodeMK == null) {
            JOptionPane.showMessageDialog(this, "Pilih dulu data pada tabel yang ingin dihapus.",
                    "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this,
                "Yakin ingin menghapus data \"" + selectedKodeMK + "\"?",
                "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        String sql = "DELETE FROM matakuliah WHERE kode_mk = ?";

        try (Connection conn = DBconnection.getConnection();
                PreparedStatement ps = conn.prepareStatement(sql)) {

            ps.setString(1, selectedKodeMK);
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
            tampilkanData();
            resetForm();

        } catch (SQLException ex) {
            logger.log(java.util.logging.Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Gagal menghapus data: " + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
                                                 
    }//GEN-LAST:event_jButtonDELETEActionPerformed

