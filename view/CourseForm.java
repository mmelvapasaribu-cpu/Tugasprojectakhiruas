    private void isiFormDariTabel() {
        int row = jTable1.getSelectedRow();
        selectedKodeMK = jTable1.getValueAt(row, 1).toString();
        jTextFieldKodeMK.setText(selectedKodeMK);
        jTextFieldNama.setText(jTable1.getValueAt(row, 2).toString());
        jTextFieldSKS.setText(jTable1.getValueAt(row, 3).toString());
        jComboBoxSM.setSelectedItem(jTable1.getValueAt(row, 4).toString());
    }

    /**
     * 
     */
    private void resetForm() {
        jTextFieldKodeMK.setText("");
        jTextFieldNama.setText("");
        jTextFieldSKS.setText("");
        jComboBoxSM.setSelectedIndex(0);
        selectedKodeMK = null;
        jTable1.clearSelection();
    }

    /**
     * 
     */
    private boolean validasiInput() {
        if (jTextFieldKodeMK.getText().trim().isEmpty()
                || jTextFieldNama.getText().trim().isEmpty()
                || jTextFieldSKS.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field wajib diisi!",
                    "Validasi", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        try {
            Integer.parseInt(jTextFieldSKS.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "SKS harus berupa angka!",
                    "Validasi", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        return true;
    }

